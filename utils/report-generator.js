function buildModules(mbtiType) {
  const template = reportLibrary[mbtiType];

  if (!template) {
    throw new Error(`Unsupported MBTI type: ${mbtiType}`);
  }

  return [
    {
      key: "asset_overview",
      title: moduleTitles.asset_overview,
      items: template.asset_overview,
    },
    {
      key: "talent_strengths",
      title: moduleTitles.talent_strengths,
      items: template.talent_strengths,
    },
    {
      key: "substitution_risks",
      title: moduleTitles.substitution_risks,
      items: template.substitution_risks,
    },
    {
      key: "career_path",
      title: moduleTitles.career_path,
      items: template.career_path,
    },
  ];
}

